    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            double p = 0;
            double q = 0;

            // Verificar si se ingresó p o q y calcular el faltante
            if (!pExito.getText().isEmpty()) {
                p = Double.parseDouble(pExito.getText());
                q = 1 - p; // Calcular q automáticamente
            } else if (!pFracaso.getText().isEmpty()) {
                q = Double.parseDouble(pFracaso.getText());
                p = 1 - q; // Calcular p automáticamente
            } else {
                JOptionPane.showMessageDialog(null, "Debe ingresar al menos un valor para p o q.", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Validar que p y q estén en el rango [0, 1]
            if (p < 0 || p > 1 || q < 0 || q > 1) {
                JOptionPane.showMessageDialog(null, "Los valores de p y q deben estar entre 0 y 1.", "Error",
                        JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Obtener los demás valores
            int n = Integer.parseInt(muestra.getText());
            int x = Integer.parseInt(nExitos.getText());
            double probabilidadAcumulada = 0;

            // Caso 1: Cálculo normal (sin acumulativos)
            if (!rbtnMenor.isSelected() && !rbtnMayor.isSelected()) {
                // Calcular el valor binomial puntual
                double resultado = calcularBinomial(n, x, p, q);
                txtResultado.setText(String.valueOf(resultado));
                return;
            }

            // Caso 2: Cálculo acumulativo para Menor o igual
            if (rbtnMenor.isSelected() && !txtMenor.getText().isEmpty()) {
                int limiteMenor = Integer.parseInt(txtMenor.getText());
                for (int i = 0; i <= limiteMenor; i++) {
                    probabilidadAcumulada += calcularBinomial(n, i, p, q);
                }
            }

            // Caso 3: Cálculo acumulativo para Mayor o igual
            if (rbtnMayor.isSelected() && !txtMayor.getText().isEmpty()) {
                int limiteMayor = Integer.parseInt(txtMayor.getText());
                for (int i = limiteMayor; i <= n; i++) {
                    probabilidadAcumulada += calcularBinomial(n, i, p, q);
                }
            }

            // Mostrar resultado acumulativo (si aplica)
            if (probabilidadAcumulada > 0) {
                txtResultado.setText(String.valueOf(probabilidadAcumulada));
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Por favor ingresa valores válidos.", "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

