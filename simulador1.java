    public static double calcularBinomial(int n, int x, double p, double q) {
        return combinatoria(n, x) * Math.pow(p, x) * Math.pow(q, n - x);
    }

    public static long combinatoria(int n, int x) {
        return factorial(n) / (factorial(x) * factorial(n - x));
    }

    public static long factorial(int num) {
        if (num == 0 || num == 1) {
            return 1;
        }
        long resultado = 1;
        for (int i = 2; i <= num; i++) {
            resultado *= i;
        }
        return resultado;
    }

